					 <h5 class="a-subtitle" id="line_cro" >
						 Passport Terms for the Sri Lanka ETA
						</h5>
						<p id="content_lead">
						   Applicants must have a valid passport, which must be valid for a minimum of six months from the intended date of arrival. ETA holders are granted a 30-day stay at a port of entry in Sri Lanka.
							</p>
					</article>
                      <article>
					 <h5 class="a-subtitle" id="line_cro" >
						  Sri Lanka ETA for children
						</h5>
						<p id="content_lead">
						   Children over 16 years of age or those who already have an individual passport must apply separately for an ETA.
							</p>
						<p id="content_lead">
						   Children under 16 years of age who are included in the parent's passport can travel to Sri lanka without the need for an individual ETA if they meet the following requirements:
							</p>
					   <ul id="litag">
						<li id="content_boutton">The child must be included in the parent/guardian’s passport.</li>
						<li id="content_boutton">The details of the child must be included in the parent/guardian’s ETA.</li>
					   </ul>
					   <p id="content_lead">
					      Failure to obtain this authorization will result in a denied entry into Sri Lanka.
