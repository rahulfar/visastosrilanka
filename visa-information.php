				<h5 class="a-subtitle" id="line_cro" >
						About the ETA for Sri Lanka
					</h5>
					<p id="content_lead">
					   The Sri Lanka ETA is an electronic travel authorization for travelers who wish to visit Sri Lanka for various purposes including tourism, transit and business. The ETA is issued through an online application system. It was introduced in January 2012 to simplify visa processes for citizens of the eligible countries who want to travel to Sri Lanka for short-term stays.
						</p>
					  <p id="content_lead">
							The Sri Lanka ETA can be obtained by completing a simple online application form. Applicants must provide their personal details, travel information and passport data.
						</p>		  
				</article>    
			 <article>
			 <h5 class="a-subtitle" id="line_cro" >
					 The Tourist and Business ETA includes the following activities
				</h5>
				<p id="content_lead">
				   The tourist ETA includes activities such as:
					</p>
					<ul id="litag">
					<li id="content_boutton">Medical treatment including Ayurvedic (herbal)</li>
					<li id="content_boutton">Participating in sporting events, competitions and activities related to cultural performance></li>
					<li id="content_boutton">Sightseeing or holidaying</li>
					<li id="content_boutton">Visiting friends and relatives</li>
					</ul>
				<p id="content_lead">
					 The Business ETA allows the holder to engage in activities such as:
					</p>
					<ul id="litag">
					<li id="content_boutton">Conferences, Workshops And Seminars</li>
					<li id="content_boutton">Participate In Art, Music And Dance Events</li>
					<li id="content_boutton">Participate In Business Meetings And Negotiations</li>
					<li id="content_boutton">Participate In Religious Events</li>
					<li id="content_boutton">Participate In Symposiums</li>
					<li id="content_boutton">Short Term Training Programs</li>
					</ul>		  
			</article>    		
			<article>
			 <h5 class="a-subtitle" id="line_cro" >
					Sri Lanka ETA Length of Authorized Stay and Validity
				</h5>
				<p id="content_lead">
				   The ETA for Sri Lanka has a validity of 6 months from the moment of approval. Travelers can apply for the Sri Lanka online ETA at any time. If the departure date is later than six months, the approved ETA will be sent prior to the trip taking into consideration the arrival date.
					</p>
					<ul id="litag">
					<li id="content_boutton">Tourist ETA: allows visitors to remain in Sri lanka for a maximum of thirty (30) days.</li>
					<li id="content_boutton">Business ETA: grants visitors a maximum stay of thirty (30) days in Sri Lanka.</li>
					<li id="content_boutton">Transit ETA: allows visitors to remain in Sri lanka for a maximum of two (2) days.</li>
					</ul>
